    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = nombreTxt.getText();
        String cedula = cedulaTxt.getText();
        String direccion = direccionTxt.getText();
        String cantidad = casillerosTxt.getText();
        
        if((! InfoReview.fieldIsEmpty(nombre))&&(! InfoReview.fieldIsEmpty(cedula))&&(! InfoReview.fieldIsEmpty(direccion))&&(! InfoReview.fieldIsEmpty(cantidad))){
            if((InfoReview.isNumber(cedula))&&(InfoReview.isNumber(cantidad))){
                theSystem = new Counter(nombre, cedula, direccion, Integer.parseInt(cantidad)); 
                menuWindow theWindow = new menuWindow(new javax.swing.JFrame(), true, theSystem);
                theWindow.setVisible(true);
            }
            else{
                InfoReview.errorMessage("La cedula y la cantidad de casilleros deben ser numeros");
            }
        }
        else{
            InfoReview.errorMessage("Debe llenar todos los espacios");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

